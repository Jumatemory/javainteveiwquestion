package October.october30.java_basic.oct31;

public class Pattern16 {
    public static void main(String[] args) {
        for (int i = 10; i>1; i--) {
            for (int j = 1; j <i ; j++) {
                System.out.print(j+" ");
            }

            System.out.println();

        }
        for (int i = 0; i<10; i++) {
            for (int j = 1; j <i ; j++) {
                System.out.print(j+" ");
            }

            System.out.println();

        }
    }
}
