package September2;

public class OneHundredSixtyFive {
    /*
    isEven method gets a number(int) if its even (2,4,8...)
     returns true.
if its odd return false.

for example:

isEven(1) --> false

isEven(8) --> true
     */
    public static boolean check(int number){
        if (number%2==0){
            return true;
        } else {
            return false;
        }
    }
}
